package javaapplication40;
public class JavaApplication40 {
    public static void main(String[] args) {
        int num, deno, i, n;
        num = 1;
        deno = 10;
        i = 1;
        n = 6;
        while (i <= n) {
            System.out.println("" + num + "/" + "" + deno);
            num = (num + 1);
            deno = (deno + 1);
            i = i + 1;
        }
    }
}
    
    

